#include "main_private.h"

//##################################################################################################################################
// DEFINES
//----------------------------------------------------------------------------------------------------------------------------------

//##################################################################################################################################
// MACROS
//----------------------------------------------------------------------------------------------------------------------------------

//##################################################################################################################################
// VARIABLES
//----------------------------------------------------------------------------------------------------------------------------------
string_t title = NULL;

//##################################################################################################################################
// FUNCTIONS
//----------------------------------------------------------------------------------------------------------------------------------
int main( int argc, string_t *argv )
{
    main_define();

    global_initialize( argc, argv, 1, 0 );

    global_finalize();
    return 1;
}

void main_define()
{
    register_initialize_routine( main_initialize );
    register_finalize_routine( main_finalize );

    string_t tmp = "UNTITLED";
    set_parameter( "General/title", ParameterString, &tmp, "The project title.", NULL, 0 );
}

void main_initialize()
{
    get_parameter_value( "General/title", ParameterString, &title );
}

void main_finalize()
{
    deallocate( title );
}